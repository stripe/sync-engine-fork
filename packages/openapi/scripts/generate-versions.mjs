#!/usr/bin/env node
/**
 * Generates src/versions.ts from committed files in oas/.
 *
 * BUNDLED_API_VERSION    — the single *.json spec file in oas/
 * SUPPORTED_API_VERSIONS — all entries in oas/manifest.json
 *
 * Both are committed. oas/manifest.json is updated by
 * docs/scripts/generate-stripe-specs.mjs (the CDN update process), which
 * writes to the same manifest file. No network access at build time.
 *
 * Run directly:  node scripts/generate-versions.mjs
 * Run via build: pnpm --filter @stripe/sync-openapi build
 */
import { readdirSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const oasDir = join(__dirname, '..', 'oas')
const outFile = join(__dirname, '..', 'src', 'versions.ts')

// BUNDLED_API_VERSION — the single committed spec file in oas/
const bundledFiles = readdirSync(oasDir).filter(
  (f) => f.endsWith('.json') && f !== 'manifest.json' && f !== 'index.html'
)
if (bundledFiles.length !== 1) {
  console.error(
    `Expected exactly 1 bundled spec in oas/, found: ${bundledFiles.join(', ') || 'none'}`
  )
  process.exit(1)
}
const bundled = bundledFiles[0].replace(/\.json$/, '')

// SUPPORTED_API_VERSIONS — from committed oas/manifest.json
// Updated by: docs/scripts/generate-stripe-specs.mjs
const manifestPath = join(oasDir, 'manifest.json')
let manifest
try {
  manifest = JSON.parse(readFileSync(manifestPath, 'utf8'))
} catch (err) {
  console.error(`Failed to read ${manifestPath}: ${err.message}`)
  console.error('Run docs/scripts/generate-stripe-specs.mjs to regenerate it.')
  process.exit(1)
}

const allVersions = Object.keys(manifest).sort().reverse() // newest first

if (allVersions.length === 0) {
  console.error('oas/manifest.json has no versions')
  process.exit(1)
}

const lines = allVersions.map((v) => `  '${v}',`).join('\n')

mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(
  outFile,
  `// Generated by scripts/generate-versions.mjs — do not edit manually.
// BUNDLED_API_VERSION: the single spec file in packages/openapi/oas/.
// SUPPORTED_API_VERSIONS: all entries in packages/openapi/oas/manifest.json.
// To update: run docs/scripts/generate-stripe-specs.mjs, then rebuild.

/** The single Stripe API spec bundled in this package (served without network). */
export const BUNDLED_API_VERSION = '${bundled}' as const

/** All Stripe API versions known to the CDN, newest first. */
export const SUPPORTED_API_VERSIONS = [
${lines}
] as const satisfies readonly string[]
`
)

console.log(`Bundled: ${bundled} | Supported: ${allVersions.length} version(s)`)
