#!/usr/bin/env node
/**
 * Generates src/versions.ts by combining:
 *   - BUNDLED_API_VERSION  — the single spec file in oas/ (local, no network)
 *   - SUPPORTED_API_VERSIONS — all versions in the CDN manifest (network fetch)
 *
 * The CDN manifest is produced by docs/scripts/generate-stripe-specs.mjs and
 * served at STRIPE_SPEC_CDN_BASE_URL/manifest.json.
 *
 * Run directly:  node scripts/generate-versions.mjs
 * Run via build: pnpm --filter @stripe/sync-openapi build
 *
 * Override the CDN URL in tests/CI:
 *   STRIPE_SPEC_CDN_BASE_URL=http://localhost:3000 node scripts/generate-versions.mjs
 */
import { readdirSync, writeFileSync, mkdirSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const oasDir = join(__dirname, '..', 'oas')
const outFile = join(__dirname, '..', 'src', 'versions.ts')

const CDN_BASE_URL =
  process.env.STRIPE_SPEC_CDN_BASE_URL ?? 'https://stripe-sync.dev/stripe-api-specs'

// BUNDLED_API_VERSION — derived from the single .json file in oas/
const bundledFiles = readdirSync(oasDir).filter(
  (f) => f.endsWith('.json') && f !== 'manifest.json' && f !== 'index.html'
)
if (bundledFiles.length !== 1) {
  console.error(
    `Expected exactly 1 bundled spec in oas/, found: ${bundledFiles.join(', ') || 'none'}`
  )
  process.exit(1)
}
const bundled = bundledFiles[0].replace(/\.json$/, '')

// SUPPORTED_API_VERSIONS — all versions from the CDN manifest
console.error(`Fetching manifest from ${CDN_BASE_URL}/manifest.json ...`)
let manifest
try {
  const res = await fetch(`${CDN_BASE_URL}/manifest.json`)
  if (!res.ok) throw new Error(`HTTP ${res.status} ${res.statusText}`)
  manifest = await res.json()
} catch (err) {
  console.error(`Failed to fetch CDN manifest: ${err.message}`)
  console.error(
    'Run with STRIPE_SPEC_CDN_BASE_URL pointing to a local mirror, or check connectivity.'
  )
  process.exit(1)
}

const allVersions = Object.keys(manifest).sort().reverse() // newest first

if (allVersions.length === 0) {
  console.error('CDN manifest has no versions')
  process.exit(1)
}

const lines = allVersions.map((v) => `  '${v}',`).join('\n')

mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(
  outFile,
  `// Generated by scripts/generate-versions.mjs — do not edit manually.
// BUNDLED_API_VERSION: derived from packages/openapi/oas/*.json (one file).
// SUPPORTED_API_VERSIONS: fetched from ${CDN_BASE_URL}/manifest.json at build time.
// Re-run \`pnpm --filter @stripe/sync-openapi build\` to pick up new CDN versions.

/** The single Stripe API spec bundled in this package (served without network). */
export const BUNDLED_API_VERSION = '${bundled}' as const

/** All Stripe API versions available via the CDN, newest first. */
export const SUPPORTED_API_VERSIONS = [
${lines}
] as const satisfies readonly string[]
`
)

console.log(`Bundled: ${bundled} | Supported: ${allVersions.length} version(s) from CDN`)
